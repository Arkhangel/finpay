"""
Три тест-кейса по ТЗ:
  (а) точно требует tool
  (б) точно не требует tool
  (в) пограничный случай
"""
from app.llm.client import run

TEST_CASES = [
    # (а) Точно требует tool — конкретный ID транзакции
    "Что случилось с транзакцией TXN-1002? Почему платёж не прошёл?",

    # (б) Точно не требует tool — общий вопрос
    "Как работает процесс возврата средств при отмене заказа?",

    # (в) Пограничный случай — упоминание проблем без конкретики
    "У меня проблемы с платежами сегодня, всё ли нормально с системой?",
]

if __name__ == "__main__":
    for i, query in enumerate(TEST_CASES, 1):
        labels = {1: "(а) требует tool", 2: "(б) не требует tool", 3: "(в) пограничный"}
        print(f"\n{'='*60}")
        print(f"Кейс {labels[i]}")
        print(f"Запрос: {query}")
        print(f"{'='*60}")
        answer = run(query)
        print(f"Ответ: {answer}")
